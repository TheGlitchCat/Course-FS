const sumar = function(a,b){
    return a + b;
}

const restar = function(a,b){
    return a - b;
}

const multiplicar = function(a,b){
    return a * b;
}

const dividir = function(a,b){
    return a / b;
}

exports.sumar = sumar;
exports.restar = restar;
exports.multiplicar = multiplicar;
exports.dividir = dividir;

